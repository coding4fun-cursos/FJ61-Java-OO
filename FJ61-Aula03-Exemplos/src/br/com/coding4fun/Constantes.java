package br.com.coding4fun;

/**
 * Classe com exemplo de declaracao de constantes (slide x).
 *
 * @author Ryan Padilha <ryan@coding4fun.com.br>
 * @since 0.1
 *
 */
public class Constantes {
	public static void main(String[] args) {
		final double PI = 3.14;
		final int MILIS = 2000;

		final double VALUE_POS_INIT;
		VALUE_POS_INIT = 900;

		System.out.println("Valor de PI = " + PI);
		System.out.println("Valor de MILIS = " + MILIS);
		System.out.println("Valor de VALUE_POS_INIT = " + VALUE_POS_INIT);
	}
}
