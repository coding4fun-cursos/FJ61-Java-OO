package br.com.coding4fun;

/**
 * Classe com exemplo de caracteres de escape (slide x).
 *
 * @author Ryan Padilha <ryan@coding4fun.com.br>
 * @since 0.1
 *
 */
public class Escape {
	public static void main(String[] args) {
		System.out.println("1. FJ61 Java e OO \b Coding4Fun.Brazil");
		System.out.println("2. FJ61 Java e OO \f Coding4Fun.Brazil");
		System.out.println("3. FJ61 Java e OO \n Coding4Fun.Brazil");
		System.out.println("4. FJ61 Java e OO \r Coding4Fun.Brazil");
		System.out.println("5. FJ61 Java e OO \t Coding4Fun.Brazil");
		System.out.println("6. FJ61 Java e OO \"Coding4Fun.Brazil\"");
		System.out.println("7. FJ61 Java e OO \'Coding4Fun.Brazil\'");
		System.out.println("8. FJ61 Java e OO \\ Coding4Fun.Brazil");
	}
}
